int		ft_sqrt(int nb)
{
	int try;

	if (nb <= 0)
		return (0);
	try = 1;
	while (try * try < nb && try < 46341)
	{
		try++;
	}
	if (nb % try == 0)
	{
		return (try);
	}
	else
	{
		return (0);
	}
}
