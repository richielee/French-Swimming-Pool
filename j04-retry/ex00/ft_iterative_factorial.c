int		ft_iterative_factorial(int nb)
{
	int ans;

	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	ans = 1;
	while (nb > 1)
	{
		ans *= nb;
		nb--;
	}
	return (ans);
}
